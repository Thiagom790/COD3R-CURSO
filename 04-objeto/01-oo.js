// Procedural
processamento(valor1, valor2, valor3);

// OO
objeto = {
  valor1,
  valor2,
  valor3,
  processamento() {
    // ...
  },
};

objeto.processamento(); // Foco passou a ser o objeto

// Princípios da OO
// 1 - Abstração
// 2 - Encapsulamento (é ter os detalhes de implementação escondidos, como o interior de uma função)
// 3 - Herança (Prefira sempre a composição em vez da herança)
// 4 - Polimorfismo (é usar algo genérico para fazer algo mais complexo, exemplo é a injeção de dependencies)
